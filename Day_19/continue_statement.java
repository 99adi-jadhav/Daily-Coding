package com.control_statements;

public class continue_statement {

	public static void main(String[] args) {
		 
		 for(int i = 1 ; i<=100 ; i++) {
			 
			  if(i<=49 ) {
				   
				  continue;
				  // after this condition number should print 50 to 100

				  
			  }
			  
			  System.out.println(i);
		 }
		 
		
	}

}
